import streamlit as st
import pandas as pd
import joblib

# Load the trained model from the same directory
model = joblib.load("house_price_model.pkl")

# Streamlit UI
st.title("üè° House Price Prediction App")

st.sidebar.header("Enter Property Details")

size = st.sidebar.slider("Size (in sqft)", 500, 5000, 1500)
bedrooms = st.sidebar.selectbox("Bedrooms", [1, 2, 3, 4, 5])
bathrooms = st.sidebar.selectbox("Bathrooms", [1, 2, 3])
year_built = st.sidebar.slider("Year Built", 1950, 2025, 2010)
has_garage = st.sidebar.radio("Has Garage?", [0, 1])
has_garden = st.sidebar.radio("Has Garden?", [0, 1])
distance = st.sidebar.slider("Distance to City Center (km)", 0.0, 30.0, 5.0)

# Create DataFrame for prediction
input_df = pd.DataFrame([{
    "Size_sqft": size,
    "Bedrooms": bedrooms,
    "Bathrooms": bathrooms,
    "Year_Built": year_built,
    "Has_Garage": has_garage,
    "Has_Garden": has_garden,
    "Distance_to_CityCenter_km": distance
}])

# Predict and display
if st.button("Predict"):
    price = model.predict(input_df)[0]
    st.success(f"üè∑Ô∏è Estimated House Price: ‚Çπ {int(price):,}")